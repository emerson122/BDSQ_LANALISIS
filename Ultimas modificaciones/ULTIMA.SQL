  `USR` varchar(50) COLLATE utf8mb3_unicode_ci NOT NULL DEFAULT 'USUARIO' COMMENT 'USUARIO',
  `NOM_USR` varchar(100) COLLATE utf8mb3_unicode_ci NOT NULL DEFAULT '' COMMENT 'NOMBRE USUARIO',
  `EST_USR` enum('BLOQUEADO','ACTIVO','INACTIVO','NUEVO') COLLATE utf8mb3_unicode_ci NOT NULL DEFAULT 'NUEVO' COMMENT 'ESTADO USUARIO',
